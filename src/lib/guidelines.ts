/** ระยะประกาศขั้นต่ำขั้นที่ 3 (เดิมขั้น 5) ตามวงเงิน e-bidding */
export function getStep5Duration(method: string, budget: number): string {
  if (method === "specific" || budget <= 500_000) {
    return "เฉพาะเจาะจง (≤500,000 บาท): ไม่ต้องประกาศ";
  }
  if (method === "selection") {
    return "วิธีคัดเลือก: ≥ 3 วันทำการ (หลังส่งหนังสือเชิญชวน)";
  }
  if (budget <= 5_000_000) return "e-bidding (500,001-5,000,000 บาท): ≥ 5 วันทำการ";
  if (budget <= 10_000_000) return "e-bidding (5,000,001-10,000,000 บาท): ≥ 10 วันทำการ";
  if (budget <= 50_000_000) return "e-bidding (10,000,001-50,000,000 บาท): ≥ 12 วันทำการ";
  return "e-bidding (>50,000,000 บาท): ≥ 20 วันทำการ";
}
